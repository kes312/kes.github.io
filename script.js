function updateNews() {
    Object.keys(newsData).forEach(key => {
        const element = document.getElementById(`news-${key}`);
        if (element) {
            const list = newsData[key];
            element.innerText = list[Math.floor(Math.random() * list.length)];
        }
    });
}

